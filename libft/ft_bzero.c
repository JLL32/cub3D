#include "libft.h"

void	ft_bzero(void *dst, size_t n)
{
	int		i;
	char	*ptr;

	ptr = dst;
	i = 0;
	while (i < (int)n)
	{
		*(ptr + i) = '\0';
		i++;
	}
}
